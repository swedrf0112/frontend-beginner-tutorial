function fenceFor(filePath) {
  if (filePath.endsWith('.css')) return 'css'
  if (filePath.endsWith('.json')) return 'json'
  if (filePath.endsWith('.html')) return 'html'
  if (filePath.endsWith('.js')) return 'js'
  return 'jsx'
}

function appendSourceAppendix(sections) {
  sections.push('## 完整原始碼附錄')
  sections.push('')
  sections.push('前面的章節是教你一步一步做，這一段才是完整對照用的最終程式碼。')
  sections.push('')

  for (const filePath of sourceFiles) {
    const absolutePath = path.join(rootDir, filePath)
    const content = fs.readFileSync(absolutePath, 'utf8').replace(/\r\n/g, '\n')
    sections.push(`### ${filePath}`)
    sections.push('')
    sections.push(`\`\`\`${fenceFor(filePath)}`)
    sections.push(content)
    sections.push('```')
    sections.push('')
  }
}

